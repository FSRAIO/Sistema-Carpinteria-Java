    private void mostrarDatos(String busqueda) {
    String[] titulos = {"ID", "Nombre", "Teléfono", "Dirección", "Email"};
    javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel(null, titulos);
    
    DAO.ClientesDAO dao = new DAO.ClientesDAO();
    
    java.util.Vector<BEAN.Clientes> lista = dao.buscarCliente(busqueda);
    
    for (BEAN.Clientes cli : lista) {
        Object[] fila = new Object[5];
        fila[0] = cli.getIdCliente();
        fila[1] = cli.getNombre();
        fila[2] = cli.getTelefono();
        fila[3] = cli.getDireccion();
        fila[4] = cli.getEmail();
        modelo.addRow(fila);
    }
    tblClientes.setModel(modelo);
}

    
