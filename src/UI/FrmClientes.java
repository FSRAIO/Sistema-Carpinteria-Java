    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked
        
    int fila = tblClientes.getSelectedRow();
    
    
    if (fila >= 0) {
        
        idClienteSeleccionado = Integer.parseInt(tblClientes.getValueAt(fila, 0).toString());
        
        
        txtNombre.setText(tblClientes.getValueAt(fila, 1).toString());
        txtTelefono.setText(tblClientes.getValueAt(fila, 2).toString());
        txtDireccion.setText(tblClientes.getValueAt(fila, 3).toString());
        txtEmail.setText(tblClientes.getValueAt(fila, 4).toString());
        
        btnGuardar.setText("ACTUALIZAR");
    }
    }//GEN-LAST:event_tblClientesMouseClicked

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // 1. VALIDACIÓN: ¿Hay alguien seleccionado?
    if (idClienteSeleccionado == 0) {
        javax.swing.JOptionPane.showMessageDialog(this, "⚠️ Primero selecciona un cliente de la tabla.");
        return;
    }

    // 2. CONFIRMACIÓN: Preguntar antes de disparar
    int respuesta = javax.swing.JOptionPane.showConfirmDialog(
            this, 
            "¿Estás seguro de eliminar a este cliente?\nEsta acción no se puede deshacer.", 
            "Confirmar Eliminación", 
            javax.swing.JOptionPane.YES_NO_OPTION
    );

    // Si dice que NO, cancelamos todo
    if (respuesta != javax.swing.JOptionPane.YES_OPTION) {
        return;
    }

    // 3. LLAMAR AL VERDUGO (DAO)
    DAO.ClientesDAO dao = new DAO.ClientesDAO();
    boolean exito = dao.eliminarCliente(idClienteSeleccionado);

    // 4. VERIFICAR RESULTADO
    if (exito) {
        javax.swing.JOptionPane.showMessageDialog(this, "✅ Cliente eliminado correctamente.");
        mostrarDatos(""); // Refrescar la tabla
        limpiar(); // Limpiar las cajas y resetear variables
    } else {
        // OJO: Si entra aquí, suele ser porque el cliente tiene PROYECTOS comprados
        javax.swing.JOptionPane.showMessageDialog(this, 
            "⛔ No se pudo eliminar.\n" +
            "Posiblemente este cliente ya tiene Proyectos registrados.\n" +
            "SQL protege los datos históricos.");
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

