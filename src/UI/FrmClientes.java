    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
            
       
    String nom = txtNombre.getText();
    String telf = txtTelefono.getText();
    String dir = txtDireccion.getText();
    String email = txtEmail.getText();
    
    if(nom.isEmpty()){
        javax.swing.JOptionPane.showMessageDialog(this, "¡El nombre es obligatorio!");
        return;
    }

    
    BEAN.Clientes cli = new BEAN.Clientes();
    cli.setNombre(nom);
    cli.setTelefono(telf);
    cli.setDireccion(dir);
    cli.setEmail(email);
    
    DAO.ClientesDAO dao = new DAO.ClientesDAO();
    boolean resp = false; 

    
    if (idClienteSeleccionado == 0) {
        
        resp = dao.registrarCliente(cli);
    } else {
        
        cli.setIdCliente(idClienteSeleccionado);
        resp = dao.actualizarCliente(cli);
    }
    if(resp){
        if(idClienteSeleccionado == 0){
             javax.swing.JOptionPane.showMessageDialog(this, "¡Guardado Nuevo!");
        } else {
             javax.swing.JOptionPane.showMessageDialog(this, "¡Actualizado Correctamente!");
        }
        mostrarDatos(""); 
        limpiar(); // ¡IMPORTANTE! Esto resetea todo
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Error al procesar");
    }
    }//GEN-LAST:event_btnGuardarActionPerformed

