    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // 1. Validar que hay cliente seleccionado
    if (txtCliente.getText().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "⚠️ Falta el cliente.");
        return;
    }

    // 2. CREAR EL OBJETO Y LLENARLO (Aquí está el truco)
    BEAN.Proyecto pro = new BEAN.Proyecto();
    
    // --- DATOS REALES (Lo que ya tienes en pantalla) ---
    // Convertimos el ID que guardó la Lupa (que es String) a Entero
    pro.setIdCliente(Integer.parseInt(UI.DigBuscarCliente.idSeleccionado)); 
    
    // Como en esta pantalla no pusiste nombre de proyecto, usamos uno genérico o el nombre del cliente
    pro.setNombreProyecto("Proyecto para " + txtCliente.getText());
    pro.setDescripcion("Cotización inicial de materiales");
    
    // Fechas (Usamos la fecha actual para inicio)
    java.sql.Date fechaHoy = new java.sql.Date(new java.util.Date().getTime());
    pro.setFechaInicio(fechaHoy);
    pro.setFechaEntregaPactada(fechaHoy); // Por ahora la misma fecha (relleno)
    
    pro.setEstado("BORRADOR"); // Estado inicial
    
    // --- DATOS FINANCIEROS (Lo que te preocupaba) ---
    // Aquí enviamos el total de materiales calculado
    double totalMateriales = Double.parseDouble(txtTotal.getText().replace(",", "."));
    pro.setCostoMateriales(totalMateriales);
    
   
    pro.setManoObraPorcentaje(0.0);
    pro.setPrecioVentaFinal(0.0);   // Aún no sabemos el precio final
    pro.setMontoAdelanto(0.0);      // Aún no pagó
    pro.setSaldoPendiente(0.0);
    
    
    DAO.ProyectoDAO dao = new DAO.ProyectoDAO();
    
    int idGenerado = dao.registrarPresupuesto(pro, modelo);

    
    if (idGenerado > 0) {
    
    
    javax.swing.JOptionPane.showMessageDialog(this, "✅ Fase 1 Guardada. Pasando a Cierre...");

    
    UI.DigCerrarVenta ventanaCierre = new UI.DigCerrarVenta(this, true, idGenerado, totalMateriales);
    ventanaCierre.setVisible(true);

    this.dispose();

} else {
    javax.swing.JOptionPane.showMessageDialog(this, "⛔ Error crítico al guardar en base de datos.");
}
    }//GEN-LAST:event_btnGuardarActionPerformed

