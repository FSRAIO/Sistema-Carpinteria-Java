    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        DigBuscarMaterial lupa = new DigBuscarMaterial(this, true);
    lupa.setVisible(true);
    
    // 2. ¿Eligió algo? (Verificamos si hay ID)
    if (DigBuscarMaterial.idSeleccionado != null && !DigBuscarMaterial.idSeleccionado.isEmpty()) {
        
        String idMat = DigBuscarMaterial.idSeleccionado;
        String nomMat = DigBuscarMaterial.nombreSeleccionado;
        String precioStr = DigBuscarMaterial.precioSeleccionado; // Ojo: Esto viene como texto
        
        // 3. Preguntar CANTIDAD (Usamos una ventanita rápida de Java)
        String cantidadStr = javax.swing.JOptionPane.showInputDialog("¿Qué cantidad deseas agregar?");
        
        // Si cancela o no escribe nada, nos salimos
        if(cantidadStr == null || cantidadStr.isEmpty()){
            return;
        }

        // 4. Cálculos Matemáticos
        double precio = Double.parseDouble(precioStr);
        int cantidad = Integer.parseInt(cantidadStr);
        double subtotal = precio * cantidad;
        
        // 5. Agregar a la Tabla (El Modelo que creamos antes)
        modelo.addRow(new Object[]{
            idMat,
            nomMat,
            precio,
            cantidad,
            subtotal
        });
        
        // 6. ¡Actualizar el Total final!
        calcularTotal();
    }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnQuitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQuitarActionPerformed
        int fila = tblDetalle.getSelectedRow();
    
    if (fila >= 0) {
        // 2. Borrar la fila del modelo
        modelo.removeRow(fila);
        
        // 3. Recalcular el total (¡Muy importante!)
        calcularTotal();
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Selecciona un ítem de la tabla para quitar.");
    }
    }//GEN-LAST:event_btnQuitarActionPerformed

