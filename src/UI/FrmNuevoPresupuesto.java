    private void configurarTabla() {
        // Definimos las columnas de nuestra "Hoja de Presupuesto"
        String[] titulos = {"ID", "Descripción Material", "Precio Unit.", "Cantidad", "Subtotal"};
        
        // Inicializamos el modelo
        modelo = new javax.swing.table.DefaultTableModel(null, titulos) {
            @Override
            public boolean isCellEditable(int row, int column) {
                // Solo dejamos editar la columna 3 (CANTIDAD) para ajustar cuántos quiere
                return column == 3; 
            }
        };
        
        tblDetalle.setModel(modelo);
        
        // Ajuste estético: Hacer la columna ID pequeña y Descripción grande
        tblDetalle.getColumnModel().getColumn(0).setPreferredWidth(30);
        tblDetalle.getColumnModel().getColumn(1).setPreferredWidth(200);
    }
    private void calcularTotal() {
    double total = 0.0;
    
    // Recorremos todas las filas de la tabla
    for (int i = 0; i < tblDetalle.getRowCount(); i++) {
        // Obtenemos el valor de la columna 4 (Subtotal)
        Object valor = tblDetalle.getValueAt(i, 4);
        
        // Sumamos (asegurando que sea número)
        total += Double.parseDouble(valor.toString());
    }
    
    // Ponemos el resultado en la caja de texto
    txtTotal.setText(String.valueOf(total));
    }

