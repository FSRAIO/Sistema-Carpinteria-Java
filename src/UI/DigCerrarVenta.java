    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        try {
        // 1. RECOLECTAR DATOS (Puede que tu papá los haya editado a mano)
        // Usamos replace(",", ".") por si pone comas decimales
        double porcentaje = Double.parseDouble(txtPorcentaje.getText().replace(",", "."));
        double precioFinal = Double.parseDouble(txtPrecioFinal.getText().replace(",", "."));
        
        // Si la caja de adelanto está vacía, es 0
        double adelanto = 0.0;
        if (!txtAdelanto.getText().isEmpty()) {
            adelanto = Double.parseDouble(txtAdelanto.getText().replace(",", "."));
        }
        
        // El saldo lo recalculamos por seguridad
        double saldo = precioFinal - adelanto;
        
        // 2. LLAMAR AL DAO PARA ACTUALIZAR EN LA BASE DE DATOS
        DAO.ProyectoDAO dao = new DAO.ProyectoDAO();
        
        // Usamos la variable 'idProyecto' que guardamos al inicio (en el constructor)
        boolean exito = dao.cerrarVenta(this.idProyecto, porcentaje, precioFinal, adelanto, saldo);
        
        if (exito) {
            javax.swing.JOptionPane.showMessageDialog(this, "✨ ¡VENTA REGISTRADA CON ÉXITO! ✨\nYa puedes cobrar.");
            this.dispose(); // Cierra la ventana final
            try {
            UTIL.GeneradorPDF pdf = new UTIL.GeneradorPDF();
            pdf.generarCotizacion(this.idProyecto);
        } catch (Exception e) {
            System.out.println("Error invocando PDF: " + e);
        }
        // ---------------------------------------------

        // Línea 234 que ya tienes:
        this.dispose();
            
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "⚠️ Ocurrió un error al actualizar los datos.");
        }
        
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor, verifica que los montos sean números válidos.");
    }
    }//GEN-LAST:event_btnConfirmarActionPerformed

