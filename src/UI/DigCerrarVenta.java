    // --- CEREBRO MATEMÁTICO ---
    private void calcular() {
        try {
            // 1. Leemos los valores (si están vacíos usamos 0)
            double porcentaje = 0;
            if(!txtPorcentaje.getText().isEmpty()) 
                porcentaje = Double.parseDouble(txtPorcentaje.getText());
            
            double gastos = 0;
            if(!txtGastos.getText().isEmpty()) 
                gastos = Double.parseDouble(txtGastos.getText());
            
            // 2. Calculamos la sugerencia
            // Regla del espejo: Costo * (Porcentaje/100)
            double ganancia = costoMateriales * (porcentaje / 100.0);
            double sugerido = costoMateriales + ganancia + gastos;
            
            // 3. Ponemos el precio sugerido (SOLO si el usuario no ha escrito uno propio a mano)
            // Truco: Para no complicarnos, actualizamos siempre el sugerido por ahora
            txtPrecioFinal.setText(String.format("%.2f", sugerido).replace(",", "."));
            
            // 4. Calculamos Saldo
            double precioFinal = Double.parseDouble(txtPrecioFinal.getText());
            double adelanto = 0;
            if(!txtAdelanto.getText().isEmpty()) 
                adelanto = Double.parseDouble(txtAdelanto.getText());
                
            double saldo = precioFinal - adelanto;
            txtSaldo.setText(String.format("%.2f", saldo).replace(",", "."));
            
        } catch (Exception e) {
            // Si escribe letras en vez de números, no hacemos nada
        }
    }
    

